# This program takes two numbers as input and adds them

# Take input from the user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Add the two numbers
sum = num1 + num2

# Display the sum
print("The sum of {0} and {1} is {2}".format(num1, num2, sum))

#Multiply the two numbers
product = num1 * num2

#Display the product
print("The product of {0} and {1} is {2}".format(num1, num2, product))

#Subtract the two numbers
difference = num1 - num2

#Display the difference
print("The difference of {0} and {1} is {2}".format(num1, num2, difference))

#Power of the two numbers
power = num1 ** num2

#Display the power
print("{0} raised to the power of {1} is {2}".format(num1, num2, power))

#Square of a number
square = num1 ** 2
print("The square of {0} is {1}".format(num1, square))